# Архивирование и разархивирование с использованием zstd и lz4
alias tarx='tar -xvf'  # Распаковать архив tar с выводом информации
alias tarc='tar -cvf'  # Создать архив tar с выводом информации

# zstd и lz4 команды для сжатия и разжатия файлов
alias zc='zstd -z'   # Сжать файл с помощью zstd 
alias zdc='zstd -d'  # Распаковать файл zstd 
alias lzc='lz4 -z'   # Сжать файл с помощью lz4 
alias lzd='lz4 -d'   # Распаковать файл lz4 

# Создание zip-архивов и распаковка zip-файлов
alias zipf='zip -r'  # Создать zip-архив рекурсивно
alias unzipf='unzip'  # Распаковать zip-архив
 
